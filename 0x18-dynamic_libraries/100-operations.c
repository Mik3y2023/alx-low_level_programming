/**
 * add - function that adds 2 intergers
 * @a: first number to add
 * @b: second number to add
 *
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subraction
 * @a: number
 * @b: nuber
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplication
 * @a: number
 * @b: nuber
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - division
 * @a: number
 * @b: nuber
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulus
 * @a: number
 * @b: nuber
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
